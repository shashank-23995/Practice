const express=require('express')
const app=express()
port=3000

app.route('/book')
  .get(function (req, res) {
    res.send('Get a random book')
  })
  .post(function (req, res) {
    res.send('Add a book')
  })
  .put(function (req, res) {
    res.send('Update the book')
  })

app.listen(port,()=>console.log(`app listening on port ${port}!`))